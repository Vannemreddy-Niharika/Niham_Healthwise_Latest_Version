/**
 * Get translation for a term
 * @param {string} category - Category (diseases, symptoms, general, diseaseContent)
 * @param {string} term - Term to translate
 * @param {string} targetLang - Target language code
 * @returns {string|null} Translated term or null if not found
 */
function getDictionaryTranslation(category, term, targetLang) {
  if (!TRANSLATION_DICTIONARY[category]) return null;
  if (!TRANSLATION_DICTIONARY[category][targetLang]) return null;
  
  const normalizedTerm = term.toLowerCase().trim();
  return TRANSLATION_DICTIONARY[category][targetLang][normalizedTerm] || null;
}

/**
 * Get all translations for a category in a language
 * @param {string} category - Category
 * @param {string} lang - Language code
 * @returns {Object} All translations for that category
 */
function getCategoryTranslations(category, lang) {
  if (!TRANSLATION_DICTIONARY[category]) return {};
  return TRANSLATION_DICTIONARY[category][lang] || {};
}

/**
 * Check if dictionary has translation
 * @param {string} category - Category
 * @param {string} term - Term to check
 * @param {string} targetLang - Target language code
 * @returns {boolean}
 */
function hasDictionaryEntry(category, term, targetLang) {
  return getDictionaryTranslation(category, term, targetLang) !== null;
}

